static char2uartParser parser;

/*******************************************************************************
 *******************************************************************************
                        GLOBAL FUNCTION DEFINITIONS
 *******************************************************************************
 ******************************************************************************/

/* Función que se llama 1 vez, al comienzo del programa */
void App_Init (void)
{
	initChar2UartParser(&parser);


}

/* Función que se llama constantemente en un ciclo infinito */
void App_Run (void)
{
	PushBit(&parser, 1);
	PushBit(&parser, 0);
	PushBit(&parser, 0);
	PushBit(&parser, 0);
	PushBit(&parser, 0);
	PushBit(&parser, 1);
	PushBit(&parser, 1);
	PushBit(&parser, 0);

	if(IsNewByte(&parser)){
		printf("%x\n", GetByte(&parser));
	}

	Push8Bit(&parser, 0x76);

	if(IsNewByte(&parser)){
		printf("%x\n", GetByte(&parser));
	}
}


